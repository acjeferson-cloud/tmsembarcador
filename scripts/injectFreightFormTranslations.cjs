const langs = ['pt', 'en', 'es'];

for (const lang of langs) {
  const path = 'c:/Users/usuário/Desktop/TmsEmbarcador/tmsembarcador/src/locales/' + lang + '/translation.json';
  if (fs.existsSync(path)) {
    let raw = fs.readFileSync(path, 'utf8');
    let json = JSON.parse(raw);
    
    if (!json.carriers.freightRates.form) {
       json.carriers.freightRates.form = {};
    }
    json.carriers.freightRates.form = Object.assign({}, json.carriers.freightRates.form, translations[lang].freightRates.form);
    
    fs.writeFileSync(path, JSON.stringify(json, null, 2));
    console.log('Merged ' + lang);
  }
}
