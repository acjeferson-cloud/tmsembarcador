const languages = ['pt', 'en', 'es'];

languages.forEach(lang => {
  const filePath = path.join(localesDir, lang, 'translation.json');
  if (fs.existsSync(filePath)) {
    const fileContent = fs.readFileSync(filePath, 'utf8');
    const json = JSON.parse(fileContent);

    if (!json.nps) {
      json.nps = {};
    }

    json.nps.dashboard = { ...json.nps.dashboard, ...npsTranslations[lang].dashboard };
    // remove correct keys so we don't end up with both modalCalculation and modalHowCalc inside the JSON forever
    if (json.nps.dashboard.modalCalculation) {
        delete json.nps.dashboard.modalCalculation;
    }
    if (json.nps.dashboard.modalReceivedReviews) {
        delete json.nps.dashboard.modalReceivedReviews;
    }

    fs.writeFileSync(filePath, JSON.stringify(json, null, 2), 'utf8');
    console.log(`Updated ${lang}/translation.json`);
  }
});
